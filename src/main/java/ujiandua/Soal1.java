package ujiandua;

public class Soal1 {
        public static void main(String[] args) {
            for (int i = 1; i < 10; i += 2) {
                System.out.print(i + "_");
            }
            System.out.println("");
            for (int n = 3; n < 8; n += 2) {
                System.out.print(n + "_");
            }
            System.out.println("");
            for (int x = 3; x < 8; x += 2) {
                System.out.print(x + "_");
            }
            System.out.println("");
            for (int u = 5; u < 6; u++) {
                System.out.print("_" + u + "_");
            }
            System.out.println("");
            for (int u = 5; u < 6; u++) {
                System.out.print(u);
            }
        }
    }

